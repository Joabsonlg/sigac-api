package io.github.joabsonlg.sigac_api.maintenance.enumeration;

/**
 * Enum que representa os tipos possíveis de manutenção.
 */
public enum MaintenanceType {
    /** Manutenção preventiva planejada */
    PREVENTIVA,

    /** Manutenção corretiva após falha */
    CORRETIVA,

    /** Manutenção preditiva baseada em dados */
    PREDITIVA
}