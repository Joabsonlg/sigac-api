package io.github.joabsonlg.sigac_api.vehicle.enumeration;

/**
 * Enum que representa os possíveis status de um veículo no sistema.
 */
public enum VehicleStatus {

    /**
     * O veículo está disponível e pode ser utilizado.
     */
    DISPONIVEL,

    /**
     * O veículo está indisponível para uso.
     */
    INDISPONIVEL,

    /**
     * O veículo está em manutenção e temporariamente fora de operação.
     */
    MANUTENCAO
}
